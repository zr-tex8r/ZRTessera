package ZRWidthUcs;
use strict qw( refs vars subs );
require Exporter;
our @ISA = qw( Exporter );
our @EXPORT = qw( 
  width_ja width_ko width_sc width_tc
);
our @EXPORT_OK = qw(
  CL_JA CL_KO CL_SC CL_TC width_ucs
);
our (@width_range, @width_range_base);
our (@sip_valid, @sip_valid_save);

use constant {
  CL_JA => 0, CL_KO => 1, CL_SC => 2, CL_TC => 3
};
sub width_ja { return width_ucs(CL_JA, $_[0]); }
sub width_ko { return width_ucs(CL_KO, $_[0]); }
sub width_sc { return width_ucs(CL_SC, $_[0]); }
sub width_tc { return width_ucs(CL_TC, $_[0]); }

sub width_ucs {
  my ($ccol, $uc) = @_;
  (defined $sip_valid_save[$ccol]) or gen_sip_map($ccol);
  if ($uc >= 0x20000) { return $sip_valid_save[$ccol]{$uc}; }
  my $res = width_ucs_sub($width_range[$ccol], $uc);
  if (defined $res) { return $res; }
  return width_ucs_sub(\@width_range_base, $uc);
}

sub width_ucs_sub {
  my ($list, $uc) = @_;
  foreach my $ent (@$list) {
    if ($uc < $ent->[1]) { return undef; }
    if ($uc <= $ent->[2]) { return $ent->[0]; }
  }
  return undef;
}

sub gen_sip_map {
  my ($ccol) = @_; my %map;
  my $ucs = $sip_valid[$ccol];
  foreach my $uc (@$ucs) { $map{$uc} = 1; }
  $sip_valid_save[$ccol] = \%map;
}

